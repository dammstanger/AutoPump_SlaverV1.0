/********************************************************************************
 * 函数名：DS3231_Init()
 * 描述  ：DS3231初始化
 * 输入  ：
 * 返回  ：-
 * 调用  ：-
 ********************************************************************************/
void DS3231_Init(void)  
{
	uchar temp;
	temp = IIC_ReadReg(DS3231_SLADDR,0x00);
	SendString("time:\r\n");									//调试信息时候用
	SendByteASCII(temp);
	SendString("\r\n");									//调试信息时候用
	

}


