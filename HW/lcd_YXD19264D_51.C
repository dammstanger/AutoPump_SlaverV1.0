//
void LCD_Delay(uint n)
{	
    unsigned char a,b;
	for(;n>0;n--)
	    for(b=20;b>0;b--)
	        for(a=50;a>0;a--);		//209
}

void LCD_Delay_us(uint n)
{	
	for(;n>0;n--);
}



/****************************************************************************
【功能说明】 LCD写字节函数
入口参数    inst=cmd/data: 命令/数据标志(data:写显示数据 cmd：写控制指令) 
                         dat : 命令/数据内容
****************************************************************************/
void LCD_Write(uchar unit,uchar inst, uchar dat)
{
	if(unit&LCD_U1)
	LCD_CS1_0;    //选中U1
	if(unit&LCD_U2)
	LCD_CS2_0;    //选中U2
	if(unit&LCD_U3)
	LCD_CS3_0;    //选中U3

	LCD_EN_1;
	if(inst)LCD_DI_1;		//数据
	else	LCD_DI_0;		//指令
	LCD_RW_0;				//写
	LCD_WriteData((uint)dat);		
	LCD_EN_0;				//锁存数据
	
	LCD_CS1_1;   
	LCD_CS2_1;    
	LCD_CS3_1;	
	LCD_Delay(1);
}


/****************************************************************************
【功能说明】 LCD_读字节函数
入口参数    uint 要读取的控制器单元
			inst=cmd/data: 命令/数据标志(data:写显示数据 cmd：写控制指令) 
                         
****************************************************************************/

uchar LCD_Read(uchar unit, uchar inst)
{
	uchar temp = 0;
	
	LCD_WriteData(0xff);	//十分重要，相当于将接口上拉，才能得到正确的数
	if(unit==LCD_U1)
		LCD_CS1_0;    		//选中U1
	else if(unit==LCD_U2)
		LCD_CS2_0;    		//选中U2
	else if(unit==LCD_U3)
		LCD_CS3_0;    		//选中U3

	if(inst)LCD_DI_1;		//数据
	else	LCD_DI_0;		//指令
	LCD_RW_1;				//读
	_nop_();				//地址建立时间min=140ns	
	LCD_EN_1;	
	_nop_();				//EN高电平时间min=450ns
	temp = LCD_ReadData();	//
	LCD_EN_0;				//锁存数据	
	LCD_RW_0;
	LCD_CS1_1;   
	LCD_CS2_1;   
	LCD_CS3_1;   
	LCD_Delay_us(1);
	return temp;
}

/****************************************************************************
【功能说明】 LCD 判忙		返回的状态寄存器的最高位为1时，表示忙碌
****************************************************************************/
void LCD_BUSYCHECK(uchar unit)
{
	uchar retval;
	do{
		retval=0;									//在多次循环中需要重新赋值
		if(unit&LCD_U1)
			retval|=LCD_Read(LCD_U1,LCD_Write_Con_Cmd);
		if(unit&LCD_U2)
			retval|=LCD_Read(LCD_U2,LCD_Write_Con_Cmd);
		if(unit&LCD_U3)
			retval|=LCD_Read(LCD_U3,LCD_Write_Con_Cmd);
	}while(retval&0x80);
}
/****************************************************************************
【功能说明】 LCD 重启
****************************************************************************/
void LCD_Reset(void)
{ 
   LCD_RST_0; 
   LCD_Delay(80);
   LCD_RST_1; 
   LCD_Delay(160);
}

/****************************************************************************
【功能说明】 LCD 清屏
****************************************************************************/
void LCD_Clear(void)
{ 
	uchar i,j;
	LCD_BUSYCHECK(LCD_UALL);
	for(i=0;i<8;i++)
	{
		LCD_Write(LCD_UALL,LCD_Write_Con_Cmd,SET_STRPAG_BASE|i);
		for(j=0;j<64;j++)
		{
			LCD_Write(LCD_UALL,LCD_Write_Dis_Data,0);
		}
	}
}

/****************************************************************************
【功能说明】 初始化LCD子程序
****************************************************************************/
void LCD_Init(void)
{
	//LCD复位
	LCD_Reset();
	LCD_Write(LCD_UALL,LCD_Write_Con_Cmd,DIS_OFF);
	LCD_Delay(5);
	LCD_Write(LCD_UALL,LCD_Write_Con_Cmd,SET_STROW_BASE|0);	//设定在屏的第0行显示的是DDRAM的起始地址为第0行
	LCD_Delay(5);  
	LCD_Clear();		//清屏
	LCD_Delay(5); 
	//开显示
	LCD_Write(LCD_U1|LCD_U2|LCD_U3,LCD_Write_Con_Cmd,DIS_ON);
	LCD_Delay(5);	
}

void LCD_Fill1()
{
	uchar i,j;
	LCD_BUSYCHECK(LCD_UALL);
	LCD_Write(LCD_U1,LCD_Write_Con_Cmd,SET_STCOL_BASE|0);
	LCD_Write(LCD_U1,LCD_Write_Con_Cmd,SET_STRPAG_BASE|2);	//设定实际的行和列
	for(i=0;i<1;i++)
	{
		for(j=0;j<64;j++)
		{
			LCD_Write(LCD_U1,LCD_Write_Dis_Data,0xff);
		}
		LCD_Write(LCD_U1,LCD_Write_Con_Cmd,SET_STCOL_BASE|0);
		LCD_Write(LCD_U1,LCD_Write_Con_Cmd,SET_STRPAG_BASE|i+1+2);	//设定实际的行和列		
	}

}

