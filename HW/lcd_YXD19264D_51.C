/****************************************************************************
【功能说明】 显示ASCII码字符串
输入：*dat 字模数据的首地址，row起始行1-4，col起始列1-24		invert ture 黑白颠倒
****************************************************************************/
void LCD_Dis_ASCIIStr(uchar row,uchar col,uchar *str, bool invert)
{
	while(*str!='\0')
	{
		LCD_Dis_Char_8_16(row,col,&CharLib_ASCII[*str-32][0],invert);			//分部ASCII码，索引0对应ASCII的第32个字符
		col++;
		if(col>24)
		{
			col = 1;
			row++;
		}
		str++;
	}
}

/******************************************************************************
【功能说明】 显示十进制浮点数
   入口参数 row: 1-4行
            col: 1-24列	注意行列为最后一个8*16数字的行列位置，按从右往左的顺序增长
      		Dig_Data: 显示的十进制数 是原始数据的100倍值
******************************************************************************/
void LCD_Dis_Digital_float(uchar row, uchar col,long Dig_Data)
{
	uchar bitbuf[10]={0};
	uchar i=0,j=9;
	uchar signflag = 0;
	uchar retval = 0;
	long temp=1;
//	retval = LCD_U3_Read(LCD_Write_Con_Cmd);
//	Dig_Data = (unsigned long)retval;
	if(Dig_Data<0)				//处理正负号
	{
		signflag = 1;
		Dig_Data = -Dig_Data;
	}
	while(Dig_Data!=0) 			//查询保存每一位对应的ASCII编码
	{ 
		temp = Dig_Data%10;
		bitbuf[i++] = temp +'0';	
		Dig_Data = Dig_Data/10;
		if(i==2)
		{
			bitbuf[i++] = '.';
		}	
	}
	while(i<=3)					//数字太小时，补足小数点和整数部分
	{
		bitbuf[i++] = '0';
		if(i==2)
		{
			bitbuf[i++] = '.';
		}
	}
	if(signflag)				//负号需要处理
	{
		bitbuf[i++] = '-';
	}
	for(j=0;j<i;j++)			//s输出显示，超过本行时在下一行右边显示
	{
		LCD_Dis_Char_8_16(row,col--,&CharLib_ASCII[bitbuf[j]-32][0],FALSE);
		if(col==0)
		{
			row++;
			col = 24;
		}
	}
} 

