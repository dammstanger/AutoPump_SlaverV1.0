/******************************************************************************
【功能说明】 在指定行列填充     
【入口参数】 	uchar row: 行坐标 取值范围（1－64）
				uchar col: 列坐标 取值范围（1-192）
				uchar row_Pixel: 行像素个数 取值范围（1－64）
				uchar col_Pixel: 列像素个数 取值范围（1－192）
******************************************************************************/
void LCD_fill_Region(uchar row, uchar col, uchar row_Pixel, uchar col_Pixel)
{	
	uchar i,j,k,temp,temp1,col_first;
	uchar unit_en_o,unit_en,unit_en_last=0;
	uchar page_first,page_last,row_page_need;
	uchar row_first_inpage,row_last_inpage;
	uchar num=0;														//保存移位数据
	page_first = (row-1)/8;												//0-7
	row_first_inpage = (row-1)%8;										//0-7
	row_last_inpage = (row+row_Pixel-1)%8;
	row_page_need = (row_Pixel+row_first_inpage)/8+((row_Pixel+row_first_inpage)%8!=0);								//由1-64转到1-8 显示所需行数
	page_last = page_first+row_page_need-1;
	
	col_first = (col-1)%64;
	temp = (col-1)/64;
	unit_en_o = LCD_U1<<temp;											//计算该列隶属于那个单元，通过得到的值移位，得到单元代码
	unit_en_last = unit_en_o;
	
	if(row_first_inpage)
	{
		LCD_Write(unit_en_o,LCD_Write_Con_Cmd,SET_STCOL_BASE|col_first);
		LCD_Write(LCD_UALL,LCD_Write_Con_Cmd,SET_STRPAG_BASE|page_first);	//设定实际的行和列
		LCD_Read(unit_en_o,LCD_Write_Dis_Data);		
		for(j=0;j<col_Pixel;j++)
		{
			temp1 = ((col+j-1)/64)%3;									//对3取余，使大于2的数归入0-2范围，图片将在首页循环显示
			unit_en = LCD_U1<<temp1;
			if(unit_en_last!=unit_en)
			{	
				unit_en_last = unit_en;
				LCD_Write(unit_en,LCD_Write_Con_Cmd,SET_STCOL_BASE|0);	//跨单元列数归零			
			}
			ReadBuf_head[j] = LCD_Read(unit_en,LCD_Write_Dis_Data);
			ReadBuf_head[j] |= 0x80>>(8-row_first_inpage-1);			//如果最高位为1，则右移会出现最高位自动补1的符号保持现象
		}																//所以采取先填一个1，少移一位的做法
		unit_en_last = unit_en_o;
	}
	if(row_last_inpage)
	{
		LCD_Write(unit_en_o,LCD_Write_Con_Cmd,SET_STCOL_BASE|col_first);
		LCD_Write(LCD_UALL,LCD_Write_Con_Cmd,SET_STRPAG_BASE|page_last);	//设定实际的行和列	
		LCD_Read(unit_en_o,LCD_Write_Dis_Data);		
		for(j=0;j<col_Pixel;j++)
		{
			temp1 = ((col+j-1)/64)%3;									//对3取余，使大于2的数归入0-2范围，图片将在首页循环显示
			unit_en = LCD_U1<<temp1;
			if(unit_en_last!=unit_en)
			{	
				unit_en_last = unit_en;
				LCD_Write(unit_en,LCD_Write_Con_Cmd,SET_STCOL_BASE|0);	//跨单元列数归零			
			}
			ReadBuf_end[j] = LCD_Read(unit_en,LCD_Write_Dis_Data);
			for(k=0;k<row_last_inpage;k++)
				num = (num<<1)|1;
			ReadBuf_end[j] |= num;
			num = 0;
		}
		unit_en_last = unit_en_o;		
	}
	LCD_Write(unit_en_o,LCD_Write_Con_Cmd,SET_STCOL_BASE|col_first);
	LCD_Write(LCD_UALL,LCD_Write_Con_Cmd,SET_STRPAG_BASE|page_first);	//设定实际的行和列
	for(i=0;i<row_page_need;i++)
	{
		for(j=0;j<col_Pixel;j++)
		{
			temp1 = ((col+j-1)/64)%3;									//对3取余，使大于2的数归入0-2范围，图片将在首页循环显示
			unit_en = LCD_U1<<temp1;
			if(unit_en_last!=unit_en)
			{	
				unit_en_last = unit_en;
				LCD_Write(unit_en,LCD_Write_Con_Cmd,SET_STCOL_BASE|0);	//跨单元列数归零			
			}
			if(row_first_inpage&&row_last_inpage&&row_page_need==1)
				LCD_Write(unit_en,LCD_Write_Dis_Data,ReadBuf_head[j]&ReadBuf_end[j]);
			else 
				if(row_first_inpage&&i==0)
				LCD_Write(unit_en,LCD_Write_Dis_Data,ReadBuf_head[j]);
			else if(row_last_inpage&&i==row_page_need-1)
				LCD_Write(unit_en,LCD_Write_Dis_Data,ReadBuf_end[j]);
			else
				LCD_Write(unit_en,LCD_Write_Dis_Data,0xff);
		}
		unit_en_last = unit_en_o;										//转行时，保存上一次代号的变量也要回归到初始值，以免误进入if(unit_en_last!=unit_en)
		LCD_Write(unit_en_o,LCD_Write_Con_Cmd,SET_STCOL_BASE|col_first);
		LCD_Write(LCD_UALL,LCD_Write_Con_Cmd,SET_STRPAG_BASE|(page_first+i)%8+1);//										归入0-7范围
	}
//	LCD_Dis_Digital(2,5,row_page_need);
//	LCD_Dis_Digital(3,5,ReadBuf_head[0]);
//	LCD_Dis_Digital(4,5,ReadBuf_end[0]);
}

