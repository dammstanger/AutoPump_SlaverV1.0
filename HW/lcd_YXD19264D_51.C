/****************************************************************************
【功能说明】 初始化LCD子程序
输入：*dat 字模数据的首地址，row所在行1-4，col所在列1-12 invert ture 黑白颠倒
****************************************************************************/
void LCD_Dis_Char_16_16(uchar row,uchar col,uchar *dat, bool invert)
{
	uchar j,temp,unit_en;
	row  = (row-1)*2;
	temp = (col-1)/4;
	if(temp==0)			unit_en = LCD_U1;
	else if(temp==1)	unit_en = LCD_U2;
	else if(temp==2)	unit_en = LCD_U3;
	col = ((col-1)%4)*16;										//计算出起始的行和列及所在区域的控制器
	
	
	
	LCD_Write(unit_en,LCD_Write_Con_Cmd,SET_STCOL_BASE|col);
	LCD_Write(unit_en,LCD_Write_Con_Cmd,SET_STRPAG_BASE|row);	//设定实际的行和列
	for(j=0;j<16;j++)
	{
		if(invert)
			LCD_Write(unit_en,LCD_Write_Dis_Data,~(*dat++));
		else LCD_Write(unit_en,LCD_Write_Dis_Data,*dat++);
			
	}
	LCD_Write(unit_en,LCD_Write_Con_Cmd,SET_STCOL_BASE|col);
	LCD_Write(unit_en,LCD_Write_Con_Cmd,SET_STRPAG_BASE|row+1);
	for(j=0;j<16;j++)
	{
		if(invert)
			LCD_Write(unit_en,LCD_Write_Dis_Data,~(*dat++));
		else LCD_Write(unit_en,LCD_Write_Dis_Data,*dat++);
	}
}

