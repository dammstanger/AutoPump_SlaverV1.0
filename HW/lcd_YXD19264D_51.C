/******************************************************************************
【功能说明】 在指定行列显示自定义图形         
【入口参数】 uchar row: 行坐标 取值范围（1-64）
            uchar col: 列坐标 取值范围（1-192）
      uchar row_Pixel: 行像素个数 取值范围（1－64）
      uchar col_Pixel: 列像素个数 取值范围（1－192）
      uchar *Dis_Data: 显示数据指针
【注意事项】     
******************************************************************************/
void LCD_Dis_Pict(uchar row, uchar col, uchar row_Pixel, uchar col_Pixel,uchar *Dis_Data)
{
	uchar i,j,temp,temp1;
	uchar page_first,col_first;
	uchar unit_en_o,unit_en,unit_en_last=0;
	
	row_Pixel = (row_Pixel-1)/8+1;									//由1-64转到1-8 显示所需行数
	page_first = (row-1)/8;											//0-7

	col_first = (col-1)%64;
	temp = (col-1)/64;
	unit_en_o = LCD_U1<<temp;										//计算该列隶属于那个单元，通过得到的值移位，得到单元代码
	unit_en_last = unit_en_o;
	
	LCD_Write(unit_en_o,LCD_Write_Con_Cmd,SET_STCOL_BASE|col_first);
	LCD_Write(LCD_UALL,LCD_Write_Con_Cmd,SET_STRPAG_BASE|page_first);	//设定实际的行和列
	for(i=0;i<row_Pixel;i++)
	{
		for(j=0;j<col_Pixel;j++)
		{
			temp1 = ((col+j-1)/64)%3;									//对3取余，使大于2的数归入0-2范围，图片将在首页循环显示
			unit_en = LCD_U1<<temp1;
			if(unit_en_last!=unit_en)
			{	
				unit_en_last = unit_en;
				LCD_Write(unit_en,LCD_Write_Con_Cmd,SET_STCOL_BASE|0);	//跨单元列数归零			
			}
			LCD_Write(unit_en,LCD_Write_Dis_Data,*Dis_Data++);
		}
		unit_en_last = unit_en_o;										//转行时，保存上一次代号的变量也要回归到初始值，以免误进入if(unit_en_last!=unit_en)
		LCD_Write(unit_en_o,LCD_Write_Con_Cmd,SET_STCOL_BASE|col_first);
		LCD_Write(LCD_UALL,LCD_Write_Con_Cmd,SET_STRPAG_BASE|(page_first+i)%8+1);//										归入0-7范围
	}
}

/******************************************************************************
【功能说明】 读指定区域的DRAM数据           
【入口参数】 	uchar row: 行坐标 取值范围（1-8）
				uchar col: 列坐标 取值范围（1-192）
				uchar row_Page: 行页数 取值范围（1－8）
				uchar col_Pixel: 列像素个数 取值范围（1－192）
【注意事项】      行只能按页读取
******************************************************************************/
void LCD_Read_Region(uchar row, uchar col, uchar row_Page, uchar col_Pixel,uchar* buf)
{
	uchar i,j;
	uchar temp,temp1;
	uchar col_first;
	uchar unit_en_o,unit_en,unit_en_last=0;
	
	col_first = (col-1)%64;
	temp = (col-1)/64;
	unit_en_o = LCD_U1<<temp;												//计算该列隶属于那个单元，通过得到的值移位，得到单元代码
	unit_en_last = unit_en_o;
	
	LCD_Write(unit_en_o,LCD_Write_Con_Cmd,SET_STCOL_BASE|col_first);
	LCD_Write(LCD_UALL,LCD_Write_Con_Cmd,SET_STRPAG_BASE|row-1);			//设定实际的行和列
	LCD_Read(unit_en_o,LCD_Write_Dis_Data);									//虚读一下
	for(i=0;i<row_Page;i++)
	{
		for(j=0;j<col_Pixel;j++)
		{
			temp1 = ((col+j-1)/64)%3;										//对3取余，使大于2的数归入0-2范围，图片将在首页循环显示
			unit_en = LCD_U1<<temp1;
			if(unit_en_last!=unit_en)
			{	
				unit_en_last = unit_en;
				LCD_Write(unit_en,LCD_Write_Con_Cmd,SET_STCOL_BASE|0);		//跨单元列数归零			
			}
			buf[j+i*col_Pixel] =  LCD_Read(unit_en,LCD_Write_Dis_Data);
		}
		unit_en_last = unit_en_o;											//转行时，保存上一次代号的变量也要回归到初始值，以免误进入if(unit_en_last!=unit_en)
		LCD_Write(unit_en_o,LCD_Write_Con_Cmd,SET_STCOL_BASE|col_first);
		LCD_Write(LCD_UALL,LCD_Write_Con_Cmd,SET_STRPAG_BASE|(row+i)%8);	//
		LCD_Read(unit_en_o,LCD_Write_Dis_Data);								//虚读一下
		
	}
	LCD_BUSYCHECK(LCD_UALL);
	LCD_Write(LCD_U2,LCD_Write_Con_Cmd,SET_STCOL_BASE|col_first);
	LCD_Write(LCD_U2,LCD_Write_Con_Cmd,SET_STRPAG_BASE|row-1);	//设定实际的行和列
	for(i=0;i<row_Page;i++)
	{
		for(j=0;j<col_Pixel;j++)
		{
			LCD_Write(LCD_U2,LCD_Write_Dis_Data,buf[j+i*col_Pixel]);
		}
		LCD_Write(LCD_U2,LCD_Write_Con_Cmd,SET_STCOL_BASE|col_first);
		LCD_Write(LCD_U2,LCD_Write_Con_Cmd,SET_STRPAG_BASE|(row+i)%8);	//设定实际的行和列		
	}
}


